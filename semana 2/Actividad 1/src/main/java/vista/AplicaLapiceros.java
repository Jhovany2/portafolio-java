    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try {
        double precioDocena = Double.parseDouble(txtPrecioDocenas.getText());
        int cantidadDocenas = Integer.parseInt(txtCantidadDocenas.getText());

        double montoCompra = precioDocena * cantidadDocenas;
        double descuento;
        int obsequio;

        if (cantidadDocenas >= 3) {
            descuento = montoCompra * 0.15;
            obsequio = 6; // media docena
        } else {
            descuento = montoCompra * 0.10;
            obsequio = 3; // cuarto de docena
        }

        double montoPagar = montoCompra - descuento;

        lblMontoCompra.setText("Monto de la compra: S/. " + montoCompra);
        lblDescuento.setText("Descuento: S/. " + descuento);
        lblMontoPagar.setText("Monto a pagar: S/. " + montoPagar);
        lblObsequio.setText("Lapiceros de obsequio: " + obsequio);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Ingrese datos v√°lidos.");
    }
    }//GEN-LAST:event_btnCalcularActionPerformed

