    // üé® Dibujo del tri√°ngulo (aproximado)
    @Override
    protected void paintComponent(Graphics g) {
        super.paintComponent(g);
        try {
            double a = obtenerLado1();
            double b = obtenerLado2();
            double c = obtenerLado3();

            if (a > 0 && b > 0 && c > 0) {
                // Ajustar tama√±o visual
                int escala = 2;
                int base = (int) (a * escala);
                int x1 = 260, y1 = 200;
                int x2 = x1 + base, y2 = 200;
                int x3 = x1 + base / 2, y3 = 200 - (int) (Math.sqrt(b * b - (base / 2) * (base / 2)) * escala);

                g.setColor(Color.GREEN);
                g.drawPolygon(new int[]{x1, x2, x3}, new int[]{y1, y2, y3}, 3);
            }
        } catch (Exception e) {
            // si hay error, no dibujar
        }
    }

